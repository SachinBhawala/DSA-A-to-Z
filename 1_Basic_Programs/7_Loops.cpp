#include<bits/stdc++.h>
using namespace std;

int main() {
    // For loop
    for (int i = 1; i <= 5; i++) {
        cout << "For Loop Iteration: " << i << endl;
    }

    // While loop
    int j = 1;
    while (j <= 5) {
        cout << "While Loop Iteration: " << j << endl;
        j++;
    }

    // Do-while loop
    int k = 1;
    do {
        cout << "Do-While Loop Iteration: " << k << endl;
        k++;
    } while (k <= 5);

    return 0;
}
// This program demonstrates the use of for, while, and do-while loops in C++.